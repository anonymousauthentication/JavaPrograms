package Java100Programs;

public class indexOf {
	public static void main(String args[]) {
		String s1 = "I Love my country , my name is john";
		System.out.println(s1.indexOf("I"));
		System.out.println(s1.indexOf("Love"));
		System.out.println(s1.indexOf("e my"));
		System.out.println(s1.indexOf("my" , 9));
		System.out.println(s1.indexOf("my"));
	}
}
